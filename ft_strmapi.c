#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*strmapi;

	i = 0;
	if (!s)
		return (NULL);
	if (!(strmapi = malloc(sizeof(*strmapi) * (ft_strlen(s) + 1))))
		return (NULL);
	while (s[i])
	{
		strmapi[i] = f(i, s[i]);
		i++;
	}
	strmapi[i] = '\0';
	return (strmapi);
}
