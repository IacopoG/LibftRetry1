#include "libft.h"

int	ft_atoi(const char *s)
{
	int		sign;
	long	num;

	sign = 1;
	num = 0;
	while (*s == 32 || (*s >= 9 && *s <= 13))
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			sign = -1;
		s++;
	}
	while (*s >= '0' && *s <= '9')
	{
		num *= 10;
		num += *s - '0';
		if (num < 0)
			return (sign > 0 ? -1 : 0);
		s++;
	}
	return ((int)(sign > 0 ? num : -num));
}
